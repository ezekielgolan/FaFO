def classify_pdf_document(
    client: OpenAI,
    model: str,
    detail: str,
    data_url: Optional[str],
    text: str,
    reporter: StatusReporter = None,
    label: str = "pdf",
) -> dict:
    schema = {
        "type": "object",
        "properties": {
            "doc_type": {
                "type": "string",
                "enum": ["bill", "statement", "report", "image", "other", "could_not_classify"],
            },
            "brand": {"type": "string"},
            "subject": {"type": "string"},
            "date": {"type": "string"},
        },
        "required": ["doc_type", "brand", "subject", "date"],
        "additionalProperties": False,
    }

    prompt = (
        "Classify this PDF. Use doc_type: bill, statement, report, image, other, or could_not_classify. "
        "If a recognizable brand/logo is present, set brand to its name (e.g., TELUS, BC Hydro, RBC). "
        "Provide a short subject (3-10 words) that would be useful in a filename. "
        "Extract the most relevant document date and format it as YYYY-MM-DD (empty string if unknown). "
        "If not sure, set brand to empty string. If could_not_classify, subject can be empty."
    )

    content = [{"type": "input_text", "text": prompt}]
    if text:
        content.append({"type": "input_text", "text": text[:6000]})
    if data_url:
        content.append({"type": "input_image", "image_url": data_url, "detail": detail})

    emit_status(reporter, f"OpenAI PDF classification started for {label}")
    started = time.time()
    response = client.responses.create(
        model=model,
        input=[{"role": "user", "content": content}],
        text={
            "format": {
                "type": "json_schema",
                "name": "pdf_label",
                "schema": schema,
                "strict": True,
            }
        },
    )

    emit_status(reporter, f"OpenAI PDF classification finished for {label} in {time.time() - started:.1f}s")
    raw = response.output_text
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        return {"doc_type": "could_not_classify", "brand": "", "subject": "", "date": ""}


def extract_pdf_first_page_image(path: Path) -> Optional[Tuple[bytes, str]]:
    global MISSING_PYMUPDF_WARNED
    try:
        import fitz  # PyMuPDF
    except Exception:
        if not MISSING_PYMUPDF_WARNED:
            print("Warning: PyMuPDF not installed. PDF OCR will be skipped.", file=sys.stderr)
            MISSING_PYMUPDF_WARNED = True
        return None

    try:
        doc = fitz.open(path)
        if doc.page_count < 1:
            return None
        page = doc.load_page(0)
        pix = page.get_pixmap()
        data = pix.tobytes("png")
        return data, "image/png"
    except Exception:
        return None
    finally:
        try:
            doc.close()
        except Exception:
            pass


def extract_video_frame(path: Path) -> Optional[Path]:
    if not shutil.which("ffmpeg"):
        return None
    tmp_dir = Path(tempfile.mkdtemp(prefix="rename_media_"))
    frame_path = tmp_dir / "frame.jpg"
    cmd = [
        "ffmpeg",
        "-y",
        "-ss",
        "0",
        "-i",
        str(path),
        "-frames:v",
        "1",
        "-q:v",
        "2",
        str(frame_path),
    ]
    try:
        subprocess.run(cmd, check=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
    except Exception:
        return None
    if frame_path.exists():
        return frame_path
    return None


