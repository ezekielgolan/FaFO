def is_autogenerated(stem: str) -> bool:
    s = stem.strip().lower()
    if not s:
        return True
    for pat in AUTOGEN_PATTERNS:
        if re.fullmatch(pat, s):
            return True

    # Mostly digits/symbols, no spaces, short-ish
    if " " not in s:
        digits = sum(ch.isdigit() for ch in s)
        letters = sum(ch.isalpha() for ch in s)
        if digits >= max(6, int(0.6 * len(s))) and letters <= 2:
            return True

    # Long hex-looking strings
    if re.fullmatch(r"[0-9a-f]{16,}", s):
        return True

    return False


def is_human_readable(stem: str) -> bool:
    s = stem.strip()
    if not s:
        return False
    if is_autogenerated(s):
        return False

    tokens = [t for t in re.split(r"[ _\-]+", s) if t]
    word_tokens = [t for t in tokens if re.search(r"[A-Za-z]", t)]
    if len(word_tokens) >= 2:
        return True

    # Any alphabetic with spaces or longer length
    if re.search(r"[A-Za-z]", s) and (" " in s or len(s) >= 14):
        return True

    return False


def is_mnemonically_poor(stem: str) -> bool:
    s = stem.strip()
    if not s:
        return True
    if "could_not_classify" in s.lower():
        return False
    lower = s.lower()
    if re.search(r"[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}", lower):
        return True
    digits = sum(ch.isdigit() for ch in s)
    letters = sum(ch.isalpha() for ch in s)
    seps = sum(ch in "-_." for ch in s)
    if len(s) >= 32 and " " not in s:
        return True
    if digits >= max(6, int(0.4 * len(s))) and letters <= int(0.4 * len(s)):
        return True
    if seps >= 4 and digits >= 6:
        return True
    return False


def sanitize_filename(name: str) -> str:
    # Replace characters that are risky across filesystems
    cleaned = re.sub(r"[\\/:*?\"<>|]", "-", name)
    cleaned = re.sub(r"\s+", " ", cleaned).strip()
    return cleaned


def ensure_unique_path(path: Path) -> Path:
    if not path.exists():
        return path
    base = path.stem
    ext = path.suffix
    parent = path.parent
    i = 2
    while True:
        candidate = parent / f"{base} - {i}{ext}"
        if not candidate.exists():
            return candidate
        i += 1


def extract_date_from_stem(stem: str) -> str:
    s = stem.lower()
    # Match YYYY-MM-DD or YYYY_MM_DD
    m = re.search(r"(20\d{2})[-_](\d{2})[-_](\d{2})", s)
    if m:
        return f"{m.group(1)}-{m.group(2)}-{m.group(3)}"

    # Match YYYYMMDD
    m = re.search(r"(20\d{2})(\d{2})(\d{2})", s)
    if m:
        return f"{m.group(1)}-{m.group(2)}-{m.group(3)}"

    return ""


def parse_date_from_text(text: str) -> str:
    if not text:
        return ""
    # Prefer explicit YYYY-MM-DD or YYYY/MM/DD
    m = re.search(r"(20\d{2})[-/](\d{2})[-/](\d{2})", text)
    if m:
        return f"{m.group(1)}-{m.group(2)}-{m.group(3)}"

    # Handle DD/MM/YYYY or MM/DD/YYYY
    m = re.search(r"(\d{1,2})[-/](\d{1,2})[-/](20\d{2})", text)
    if m:
        a = int(m.group(1))
        b = int(m.group(2))
        y = m.group(3)
        # Disambiguate: if a > 12, it's DD/MM; if b > 12, it's MM/DD; else assume MM/DD (US)
        if a > 12:
            d, mo = a, b
        elif b > 12:
            mo, d = a, b
        else:
            mo, d = a, b
        return f"{y}-{mo:02d}-{d:02d}"

    return ""


def year_month_from_date(date_str: str) -> tuple[str, str]:
    if not date_str:
        return "Unknown", "Unknown"
    m = re.match(r"(20\d{2})-(\d{2})-(\d{2})", date_str)
    if not m:
        return "Unknown", "Unknown"
    return m.group(1), m.group(2)


def progress_bar(scanned: int, total: int, width: int = 50) -> str:
    if total <= 0:
        return "[" + ("?" * width) + "]"
    bar = ["-"] * width
    pct_text = f"{(scanned / total) * 100:0.1f}%"
    idx = int((scanned / total) * max(1, width - len(pct_text)))
    idx = max(0, min(width - len(pct_text), idx))
    for i, ch in enumerate(pct_text):
        bar[idx + i] = ch
    return "[" + "".join(bar) + "]"


def format_eta(seconds: float) -> str:
    seconds = max(0, int(seconds))
    h = seconds // 3600
    m = (seconds % 3600) // 60
    s = seconds % 60
    if h > 0:
        return f"{h:d}h{m:02d}m{s:02d}s"
    return f"{m:02d}m{s:02d}s"


def format_clock(timestamp: Optional[float] = None) -> str:
    if timestamp is None:
        timestamp = time.time()
    return time.strftime("%Y-%m-%d %H:%M:%S", time.localtime(timestamp))


def print_phase_progress(
    label: str,
    current: int,
    total: int,
    started: float,
    current_label: str = "scanned",
    **metrics: object,
) -> None:
    elapsed = max(1.0, time.time() - started)
    rate = current / elapsed if current > 0 else 0.0
    remaining = max(0, total - current)
    eta = format_eta(remaining / rate) if rate > 0 else "??m??s"
    bar = progress_bar(current, total)
    fields = [
        f"{current_label}={current}",
        *(f"{key}={value}" for key, value in metrics.items()),
        f"remaining={remaining}",
        f"rate={rate:.2f} files/sec",
        f"eta={eta}",
    ]
    print(
        f"\n\n{label} {bar} clock={format_clock()} elapsed={format_eta(elapsed)} "
        + " ".join(fields)
        + "\n\n"
    )


def image_to_data_url(path: Path) -> Optional[str]:
    if path.suffix.lower() == ".eps":
        tmp_dir = Path(tempfile.mkdtemp(prefix="rename_eps_"))
        png_path = tmp_dir / "preview.png"
        try:
            subprocess.run(
                ["sips", "-s", "format", "png", str(path), "--out", str(png_path)],
                check=True,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
            )
            if png_path.exists():
                return bytes_to_data_url(png_path.read_bytes(), "image/png")
        except Exception:
            return None
        finally:
            shutil.rmtree(tmp_dir, ignore_errors=True)

    mime, _ = mimetypes.guess_type(path.name)
    if not mime:
        if path.suffix.lower() in {".jpg", ".jpeg"}:
            mime = "image/jpeg"
        elif path.suffix.lower() == ".png":
            mime = "image/png"
        elif path.suffix.lower() == ".webp":
            mime = "image/webp"
        elif path.suffix.lower() == ".gif":
            mime = "image/gif"
        elif path.suffix.lower() == ".avif":
            mime = "image/avif"
        else:
            return None

    data = path.read_bytes()
    b64 = base64.b64encode(data).decode("ascii")
    return f"data:{mime};base64,{b64}"


def bytes_to_data_url(data: bytes, mime: str) -> str:
    b64 = base64.b64encode(data).decode("ascii")
    return f"data:{mime};base64,{b64}"


def load_brand_map() -> list[tuple[str, list[str]]]:
    if not BRANDS_FILE.exists():
        return []
    entries: list[tuple[str, list[str]]] = []
    for line in BRANDS_FILE.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        parts = [p.strip() for p in line.split("|") if p.strip()]
        if not parts:
            continue
        canonical = parts[0]
        aliases = parts
        entries.append((canonical, aliases))
    return entries


def load_brand_overrides() -> dict[str, str]:
    if not BRAND_OVERRIDES_FILE.exists():
        return {}
    overrides: dict[str, str] = {}
    with BRAND_OVERRIDES_FILE.open(newline="", encoding="utf-8") as f:
        reader = csv.reader(f)
        for row in reader:
            if not row or row[0].startswith("#"):
                continue
            if len(row) >= 2:
                overrides[row[0].strip()] = row[1].strip()
    return overrides


def classify_image(
    client: OpenAI,
    model: str,
    detail: str,
    data_url: str,
    reporter: StatusReporter = None,
    label: str = "image",
) -> dict:
    if not data_url:
        return {"category": "could_not_classify", "description": ""}

    schema = {
        "type": "object",
        "properties": {
            "category": {
                "type": "string",
                "enum": [
                    "document",
                    "screenshot",
                "photo",
                "schematic",
                "cartoon",
                "media",
                "application",
                "could_not_classify",
            ],
        },
            "description": {"type": "string"},
        },
        "required": ["category", "description"],
        "additionalProperties": False,
    }

    prompt = (
        "Classify the image into one of the categories: document, screenshot, "
        "photo, schematic, cartoon, media, application, could_not_classify. "
        "If you choose document/screenshot/photo/schematic/cartoon, provide a short "
        "description (3-10 words) that will be used in a filename. "
        "If you choose media, describe what the media is (3-10 words). "
        "If you choose application, describe the app or installer (3-10 words). "
        "If the image is a document and you can recognize the type, use that (e.g., bank statement). "
        "If you choose could_not_classify, set description to an empty string."
    )

    emit_status(reporter, f"OpenAI image classification started for {label}")
    started = time.time()
    try:
        response = client.responses.create(
            model=model,
            input=[
                {
                    "role": "user",
                    "content": [
                        {"type": "input_text", "text": prompt},
                        {"type": "input_image", "image_url": data_url, "detail": detail},
                    ],
                }
            ],
            text={
                "format": {
                    "type": "json_schema",
                    "name": "image_label",
                    "schema": schema,
                    "strict": True,
                }
            },
        )
    except Exception as exc:
        # If the file isn't a valid image (corrupt or mislabeled), skip gracefully.
        if "invalid image" in str(exc).lower() or "invalid_request_error" in str(exc).lower():
            emit_status(reporter, f"OpenAI image classification skipped invalid image for {label}")
            return {"category": "could_not_classify", "description": ""}
        raise
    emit_status(reporter, f"OpenAI image classification finished for {label} in {time.time() - started:.1f}s")

    raw = response.output_text
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        return {"category": "could_not_classify", "description": ""}


